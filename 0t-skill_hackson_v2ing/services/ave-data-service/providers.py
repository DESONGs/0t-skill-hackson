def _normalize_wallet_activity_item(item: dict[str, Any], *, chain: str) -> dict[str, Any]:
    from_symbol = _normalize_symbol(item.get("from_symbol"))
    to_symbol = _normalize_symbol(item.get("to_symbol"))
    from_ref = _token_reference(
        {
            "chain": chain,
            "token": item.get("from_address"),
            "token_address": item.get("from_address"),
            "identifier": f"{chain}:{_canonical_token_address(item.get('from_address'))}" if _canonical_token_address(item.get("from_address")) else f"{chain}:{from_symbol or 'unknown'}",
            "symbol": from_symbol,
            "name": from_symbol,
            "source": "ave",
        },
        chain=chain,
        fallback_source="ave",
    )
    to_ref = _token_reference(
        {
            "chain": chain,
            "token": item.get("to_address"),
            "token_address": item.get("to_address"),
            "identifier": f"{chain}:{_canonical_token_address(item.get('to_address'))}" if _canonical_token_address(item.get("to_address")) else f"{chain}:{to_symbol or 'unknown'}",
            "symbol": to_symbol,
            "name": to_symbol,
            "source": "ave",
        },
        chain=chain,
        fallback_source="ave",
    )
    from_is_quote = _is_quote_symbol(from_symbol)
    to_is_quote = _is_quote_symbol(to_symbol)
    from_amount_usd = _estimate_amount_usd(amount=item.get("from_amount"), price=item.get("from_price_usd"))
    to_amount_usd = _estimate_amount_usd(amount=item.get("to_amount"), price=item.get("to_price_usd"))
    amount_usd = _first_non_empty(to_amount_usd, from_amount_usd)

    action = "swap"
    token_ref = to_ref
    if not from_is_quote and to_is_quote:
        action = "sell"
        token_ref = from_ref
    elif from_is_quote and not to_is_quote:
        action = "buy"
        token_ref = to_ref
    elif not to_is_quote and to_ref.get("token_address"):
        action = "buy" if from_is_quote else "swap"
        token_ref = to_ref
    elif not from_is_quote and from_ref.get("token_address"):
        action = "sell" if to_is_quote else "swap"
        token_ref = from_ref
    elif from_ref.get("token_address"):
        token_ref = from_ref

    token_amount = None
    if action == "buy":
        token_amount = _safe_float(item.get("to_amount"))
    elif action == "sell":
        token_amount = _safe_float(item.get("from_amount"))

    return {
        "tx_hash": _first_non_empty(item.get("tx_hash"), item.get("hash"), item.get("transaction_hash"), item.get("transaction")),
        "timestamp": _first_non_empty(item.get("timestamp"), item.get("time"), utc_now()),
        "action": action,
        "token_ref": token_ref if isinstance(token_ref, dict) else None,
        "amount_usd": amount_usd,
        "token_amount": token_amount,
        "note": _first_non_empty(item.get("note"), item.get("description"), _activity_note(action=action, from_symbol=from_symbol, to_symbol=to_symbol)),
        "quote_symbol": to_symbol if action == "sell" else from_symbol,
        "quote_token_address": _canonical_token_address(item.get("to_address") if action == "sell" else item.get("from_address")),
        "from_symbol": from_symbol,
        "to_symbol": to_symbol,
        "from_token_address": _canonical_token_address(item.get("from_address")),
        "to_token_address": _canonical_token_address(item.get("to_address")),
        "from_amount": _safe_float(item.get("from_amount")),
        "to_amount": _safe_float(item.get("to_amount")),
    }


@dataclass(slots=True)
class _VendoredAveCli:
    script_path: Path = DEFAULT_VENDOR_SCRIPT
    python_executable: str = sys.executable
    timeout_seconds: float = 30.0

    def run_json(self, command: str, *args: str) -> dict[str, Any]:
        if not self.script_path.exists():
            raise ServiceError(
                code=ErrorCode.PROVIDER_UNAVAILABLE,
                message="Vendored AVE data-rest script is missing",
                status_code=503,
                details={"script_path": str(self.script_path)},
            )

        env = os.environ.copy()
        env.setdefault("AVE_IN_SERVER", "true")
        env.setdefault("AVE_USE_DOCKER", "false")

        completed = subprocess.run(
            [self.python_executable, str(self.script_path), command, *args],
            capture_output=True,
            text=True,
            env=env,
            timeout=self.timeout_seconds,
        )
        if completed.returncode != 0:
            stderr = (completed.stderr or completed.stdout or "").strip()
            raise ServiceError(
                code=ErrorCode.PROVIDER_ERROR,
                message=f"AVE CLI command failed: {command}",
                status_code=502,
                details={
                    "command": command,
                    "args": list(args),
                    "returncode": completed.returncode,
                    "stderr": stderr,
                    "script_path": str(self.script_path),
                },
            )

        output = (completed.stdout or "").strip()
        if not output:
            raise ServiceError(
                code=ErrorCode.PROVIDER_ERROR,
                message="AVE CLI returned empty output",
                status_code=502,
                details={"command": command, "args": list(args), "script_path": str(self.script_path)},
            )

        try:
            return _normalize_cli_payload(json.loads(output))
        except json.JSONDecodeError as exc:
            raise ServiceError(
                code=ErrorCode.PROVIDER_ERROR,
                message="AVE CLI returned invalid JSON",
                status_code=502,
                details={"command": command, "args": list(args), "stdout": output, "script_path": str(self.script_path)},
            ) from exc


@dataclass(slots=True)
class AveRestProvider:
    script_path: Path = DEFAULT_VENDOR_SCRIPT
    python_executable: str = sys.executable
    timeout_seconds: float = 10.0
    name: ProviderName = ProviderName.AVE_REST
    _cli: _VendoredAveCli = field(init=False, repr=False)

    def __post_init__(self) -> None:
        self._cli = _VendoredAveCli(
            script_path=self.script_path,
            python_executable=self.python_executable,
            timeout_seconds=self.timeout_seconds,
        )

    def _search(self, payload: DiscoverTokensRequest) -> dict[str, Any]:
        keyword = (payload.query or payload.source or payload.chain or "ave").strip()
        args = ["--keyword", keyword, "--limit", str(payload.limit)]
        if payload.chain:
            args.extend(["--chain", payload.chain])
        return self._cli.run_json("search", *args)

    def discover_tokens(self, payload: DiscoverTokensRequest) -> dict[str, Any]:
        raw = self._search(payload)
        section = _section_dict(raw)
        items = _section_list(raw, "token_refs", "items", "tokens", "results")
        token_refs = [_token_reference(_as_dict(item), chain=payload.chain, fallback_source=payload.source or "ave") for item in items]
        if not token_refs and isinstance(section, dict):
            token_refs = [_token_reference(section, chain=payload.chain, fallback_source=payload.source or "ave")]
        ranking_context = _as_dict(section.get("ranking_context"))
        if not ranking_context:
            ranking_context = {
                "title": _first_non_empty(section.get("title"), "Token discovery"),
                "window": _first_non_empty(section.get("window"), "24h"),
                "source": _first_non_empty(section.get("source"), payload.source, "ave"),
                "note": _first_non_empty(section.get("note"), "Vendored AVE CLI search result"),
                "metadata": {
                    "query": payload.query or "",
                    "chain": payload.chain or "",
                },
            }
        return {
            "token_refs": token_refs[: payload.limit],
            "ranking_context": ranking_context,
            "source_meta": _source_meta(raw, command="search", provider=self.name),
        }

    def _resolve_token_address(self, payload: DiscoverTokensRequest | InspectTokenRequest | InspectMarketRequest) -> str:
        token_ref = payload.token_ref
        chain = getattr(token_ref, "chain", None)
        if token_ref.token_address:
            candidate = _canonical_token_address(token_ref.token_address)
            if candidate is not None:
                return candidate
            if not _is_evm_chain(chain):
                return token_ref.token_address
        identifier = token_ref.identifier
        if ":" in identifier:
            suffix = identifier.split(":", 1)[1]
            candidate = _canonical_token_address(suffix)
            if candidate is not None:
                return candidate
            if not _is_evm_chain(chain):
                return suffix
        if "-" in identifier:
            suffix = identifier.rsplit("-", 1)[0]
            candidate = _canonical_token_address(suffix)
            if candidate is not None:
                return candidate
            if not _is_evm_chain(chain):
                return suffix
        if not _is_evm_chain(chain):
            return identifier
        raise ServiceError(
            code=ErrorCode.VALIDATION_ERROR,
            message="Token address is required for EVM token inspection",
            status_code=400,
            details={"identifier": identifier, "chain": chain, "token_address": token_ref.token_address},
        )

    def _resolve_chain(self, token_ref: Any) -> str:
        chain = getattr(token_ref, "chain", None)
        if chain:
            return chain
        identifier = getattr(token_ref, "identifier", "")
        if ":" in identifier:
            return identifier.split(":", 1)[0]
        if "-" in identifier:
            return identifier.rsplit("-", 1)[-1]
        return "unknown"

