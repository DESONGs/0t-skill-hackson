def _as_dict(value: Any) -> dict[str, Any]:
    return value if isinstance(value, dict) else {}


def _as_list(value: Any) -> list[Any]:
    return value if isinstance(value, list) else []


def _data_section(payload: dict[str, Any]) -> Any:
    data = payload.get("data")
    return payload if data is None else data


def _section_dict(payload: dict[str, Any]) -> dict[str, Any]:
    section = _data_section(payload)
    return section if isinstance(section, dict) else payload


def _nested_dict(payload: dict[str, Any], *keys: str) -> dict[str, Any]:
    current: Any = payload
    for key in keys:
        if not isinstance(current, dict):
            return {}
        current = current.get(key)
    return current if isinstance(current, dict) else {}


def _section_list(payload: dict[str, Any], *keys: str) -> list[Any]:
    section = _data_section(payload)
    if isinstance(section, dict):
        for key in keys:
            value = section.get(key)
            if isinstance(value, list):
                return value
    if isinstance(section, list):
        return section
    for key in keys:
        value = payload.get(key)
        if isinstance(value, list):
            return value
    return []


def _first_non_empty(*values: Any) -> Any:
    for value in values:
        if value is None:
            continue
        if isinstance(value, str) and not value.strip():
            continue
        return value
    return None


def _safe_float(value: Any) -> float | None:
    if value is None:
        return None
    if isinstance(value, str):
        text = value.strip()
        if not text or text.lower() in _PLACEHOLDER_TEXT:
            return None
        value = text
    try:
        return float(value)
    except (TypeError, ValueError):
        return None


def _normalized_text(value: Any) -> str | None:
    text = str(value or "").strip()
    if not text or text.lower() in _PLACEHOLDER_TEXT:
        return None
    return text


def _normalize_symbol(value: Any) -> str | None:
    text = _normalized_text(value)
    return text if text is None else text.strip()


def _is_evm_address(value: Any) -> bool:
    text = _normalized_text(value)
    return bool(text and _EVM_ADDRESS_RE.fullmatch(text))


def _is_evm_chain(value: Any) -> bool:
    text = _normalized_text(value)
    return bool(text and text.lower() in _EVM_CHAINS)


def _canonical_token_address(value: Any) -> str | None:
    text = _normalized_text(value)
    if text is None:
        return None
    return text.lower() if _is_evm_address(text) else None


def _is_quote_symbol(value: Any) -> bool:
    symbol = _normalize_symbol(value)
    return bool(symbol and symbol.upper() in _QUOTE_SYMBOLS)


def _estimate_amount_usd(*, amount: Any, price: Any) -> float | None:
    amount_value = _safe_float(amount)
    price_value = _safe_float(price)
    if amount_value is None or price_value is None:
        return None
    return round(amount_value * price_value, 8)


def _normalize_pair_candidate(
    entry: dict[str, Any],
    *,
    chain: str,
    fallback_token: dict[str, Any],
) -> dict[str, Any]:
    pair_address = _first_non_empty(entry.get("pair_address"), entry.get("pair"), entry.get("address"))
    token0_symbol = _normalize_symbol(entry.get("token0_symbol"))
    token1_symbol = _normalize_symbol(entry.get("token1_symbol"))
    token0_address = _canonical_token_address(entry.get("token0_address"))
    token1_address = _canonical_token_address(entry.get("token1_address"))
    target_token_address = _canonical_token_address(
        _first_non_empty(entry.get("target_token"), fallback_token.get("token_address"))
    )
    if target_token_address and token0_address == target_token_address:
        base_token = {
            "identifier": f"{chain}:{token0_address}",
            "chain": chain,
            "token_address": token0_address,
            "symbol": token0_symbol,
            "name": token0_symbol,
            "source": "ave",
        }
        quote_token = {
            "identifier": f"{chain}:{token1_address}" if token1_address else f"{chain}:{token1_symbol or 'QUOTE'}",
            "chain": chain,
            "token_address": token1_address,
            "symbol": token1_symbol,
            "name": token1_symbol,
            "source": "ave",
        }
    elif target_token_address and token1_address == target_token_address:
        base_token = {
            "identifier": f"{chain}:{token1_address}",
            "chain": chain,
            "token_address": token1_address,
            "symbol": token1_symbol,
            "name": token1_symbol,
            "source": "ave",
        }
        quote_token = {
            "identifier": f"{chain}:{token0_address}" if token0_address else f"{chain}:{token0_symbol or 'QUOTE'}",
            "chain": chain,
            "token_address": token0_address,
            "symbol": token0_symbol,
            "name": token0_symbol,
            "source": "ave",
        }
    else:
        base_token = fallback_token
        quote_token = {
            "identifier": f"{chain}:USDT" if chain else "USDT",
            "chain": chain,
            "token_address": None,
            "symbol": "USDT",
            "name": "Tether USD",
            "source": "ave",
        }
    identifier = _first_non_empty(
        entry.get("identifier"),
        entry.get("pair_identifier"),
        f"{_first_non_empty(base_token.get('symbol'), fallback_token.get('symbol'), 'TOKEN')}/{_first_non_empty(quote_token.get('symbol'), 'USDT')}",
    )
    return {
        "identifier": identifier,
        "chain": chain,
        "pair_address": pair_address,
        "dex": _first_non_empty(entry.get("dex"), entry.get("amm"), entry.get("source")),
        "base_token_ref": base_token,
        "quote_token_ref": quote_token,
        "liquidity_usd": _first_non_empty(entry.get("liquidity_usd"), entry.get("tvl")),
        "volume_24h_usd": _first_non_empty(entry.get("volume_24h_usd"), entry.get("volume_u_24h"), entry.get("volume_u")),
        "price_change_1h": _first_non_empty(entry.get("price_change_1h"), entry.get("token_price_change_1h")),
        "price_change_24h": _first_non_empty(entry.get("price_change_24h"), entry.get("price_change_1d"), entry.get("token_price_change_24h")),
        "price_usd": _first_non_empty(entry.get("price_usd"), entry.get("token1_price_usd"), entry.get("token0_price_usd")),
    }


def _best_pair_candidate(
    token_payload: dict[str, Any],
    pair_items: list[dict[str, Any]],
    *,
    chain: str,
    fallback_token: dict[str, Any],
) -> dict[str, Any]:
    main_pair = _canonical_token_address(token_payload.get("main_pair"))
    normalized = [
        _normalize_pair_candidate(item, chain=chain, fallback_token=fallback_token)
        for item in pair_items
        if isinstance(item, dict)
    ]
    if main_pair:
        for item in normalized:
            if _canonical_token_address(item.get("pair_address")) == main_pair:
                return item
    if normalized:
        normalized.sort(
            key=lambda item: (
                _safe_float(item.get("liquidity_usd")) or 0.0,
                _safe_float(item.get("volume_24h_usd")) or 0.0,
            ),
            reverse=True,
        )
        return normalized[0]
    return {}


def _activity_note(*, action: str, from_symbol: str | None, to_symbol: str | None) -> str:
    left = from_symbol or "unknown"
    right = to_symbol or "unknown"
    return f"{action}:{left}->{right}"


def _token_identifier(chain: str | None, entry: dict[str, Any]) -> str:
    token_address = _canonical_token_address(
        _first_non_empty(entry.get("token_address"), entry.get("address"), entry.get("token"))
    )
    if token_address is not None:
        return f"{chain}:{token_address}" if chain else token_address
    symbol_or_name = _first_non_empty(_normalize_symbol(entry.get("symbol")), _normalized_text(entry.get("name")), "unknown")
    return str(
        _first_non_empty(
            entry.get("identifier"),
            entry.get("token_id"),
            entry.get("token_identifier"),
            f"{chain}:{symbol_or_name}" if chain else symbol_or_name,
        )
    )


def _token_reference(entry: dict[str, Any], *, chain: str | None = None, fallback_source: str | None = None) -> dict[str, Any]:
    entry_chain = _first_non_empty(entry.get("chain"), chain)
    identifier = _token_identifier(entry_chain, entry)
    symbol = _first_non_empty(_normalize_symbol(entry.get("symbol")), _normalize_symbol(entry.get("token_symbol")))
    name = _first_non_empty(_normalized_text(entry.get("name")), _normalized_text(entry.get("token_name")))
    token_address = _canonical_token_address(_first_non_empty(entry.get("token_address"), entry.get("address"), entry.get("token")))
    if token_address is None and identifier and ":" in identifier:
        suffix = identifier.split(":", 1)[1]
        token_address = _canonical_token_address(suffix)
    return {
        "identifier": identifier,
        "chain": entry_chain,
        "token_address": token_address,
        "symbol": symbol,
        "name": name,
        "rank": entry.get("rank"),
        "score": entry.get("score"),
        "source": _first_non_empty(entry.get("source"), fallback_source),
    }


def _pair_reference(entry: dict[str, Any], *, fallback_token: dict[str, Any], fallback_chain: str | None = None) -> dict[str, Any]:
    chain = _first_non_empty(entry.get("chain"), fallback_chain, fallback_token.get("chain"))
    identifier = _first_non_empty(entry.get("identifier"), entry.get("pair_identifier"))
    if identifier is None:
        token_symbol = _first_non_empty(fallback_token.get("symbol"), fallback_token.get("identifier"), "TOKEN")
        identifier = f"{token_symbol}/USDT"
    base_token_ref = _first_non_empty(entry.get("base_token_ref"), entry.get("base_token"), fallback_token)
    if not isinstance(base_token_ref, dict):
        base_token_ref = fallback_token
    quote_token_ref = _first_non_empty(entry.get("quote_token_ref"), entry.get("quote_token"))
    if not isinstance(quote_token_ref, dict):
        quote_token_ref = {
            "identifier": _first_non_empty(entry.get("quote_identifier"), f"{chain}:USDT" if chain else "USDT"),
            "chain": chain,
            "token_address": None,
            "symbol": "USDT",
            "name": "Tether USD",
            "source": "ave",
        }
    return {
        "identifier": identifier,
        "chain": chain,
        "pair_address": _first_non_empty(entry.get("pair_address"), entry.get("address")),
        "dex": _first_non_empty(entry.get("dex"), entry.get("amm"), entry.get("source")),
        "base_token_ref": base_token_ref,
        "quote_token_ref": quote_token_ref,
    }


def _source_meta(payload: dict[str, Any], *, command: str, provider: ProviderName, request_id: str | None = None) -> dict[str, Any]:
    section = _section_dict(payload)
    raw_meta = _as_dict(payload.get("meta"))
    raw_top_source_meta = _as_dict(payload.get("source_meta"))
    raw_source_meta = _as_dict(section.get("source_meta"))
    return {
        "provider": provider,
        "request_id": _first_non_empty(
            request_id,
            raw_source_meta.get("request_id"),
            raw_top_source_meta.get("request_id"),
            raw_meta.get("request_id"),
            payload.get("request_id"),
        ),
        "fetched_at": _first_non_empty(
            raw_source_meta.get("fetched_at"),
            raw_top_source_meta.get("fetched_at"),
            raw_meta.get("fetched_at"),
            payload.get("fetched_at"),
            utc_now(),
        ),
        "cached": bool(_first_non_empty(raw_source_meta.get("cached"), raw_top_source_meta.get("cached"), raw_meta.get("cached"), False)),
        "source_version": _first_non_empty(
            raw_source_meta.get("source_version"),
            raw_top_source_meta.get("source_version"),
            raw_meta.get("source_version"),
            f"vendored-cli:{command}",
        ),
        "metadata": {
            **_as_dict(raw_meta.get("metadata")),
            **_as_dict(raw_top_source_meta.get("metadata")),
            **_as_dict(raw_source_meta.get("metadata")),
            "command": command,
        },
    }


